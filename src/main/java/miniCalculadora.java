public class miniCalculadora {

    public static double valorAbsoluto(double in) {
        return Math.abs(in);
    }

    public static double raizCuadrada(double in) {
        return Math.sqrt(in);
    }
    
    /**
     * @param a double
     * @param b double
     * @return double addition of a and b
     */
    public static double add(double a, double b) {
        return a + b;
    }
}